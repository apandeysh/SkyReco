    private void ModelNumberTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ModelNumberTxtKeyPressed
        //only accept letters, space bar,number and delete backspace
        char c= evt.getKeyChar();
        
        if(Character.isLetter(c)||Character.isWhitespace(c)||Character.isISOControl(c)||Character.isDigit(c)||c=='-'){
            ModelNumberTxt.setEditable(true);
        }
        else{
            ModelNumberTxt.setEditable(false);
            JOptionPane.showMessageDialog(this,"Special character are not accepted! Please Enter proper Model Number!");
        }
    }//GEN-LAST:event_ModelNumberTxtKeyPressed

