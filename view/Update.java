    private void CostTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CostTxtKeyTyped
        //only accept number 
        char c= evt.getKeyChar();
        
        if(Character.isDigit(c)){
            CostTxt.setEditable(true);
        }
        else{
            CostTxt.setEditable(false);
            JOptionPane.showMessageDialog(this,"Please enter only numbers");
        }
    }//GEN-LAST:event_CostTxtKeyTyped

    private void UpdateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBActionPerformed
        //checking if the textfield is empty
        if(ModelNumberTxt.getText().equals("")||NameTxt.getText().equals("")||CompanyTxt.getText().equals("")||
                CostTxt.getText().equals("")||CameraTxt.getText().equals("")||SNtxt.getText().equals("")||Rangetxt.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please!! Enter all the value!");
        }
        else{
            home.UpdateRow(new Object[]{
            SNtxt.getText(),
            ModelNumberTxt.getText(),
            NameTxt.getText(),
            CompanyTxt.getText(),
            CameraTxt.getText(),
            Rangetxt.getText(),
            CostTxt.getText(),
            PurposeComboBox.getSelectedItem()
            });
            JOptionPane.showMessageDialog(this,"Values are Updated");
            close();
            
        }
    }//GEN-LAST:event_UpdateBActionPerformed

