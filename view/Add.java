    private void AddB2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddB2ActionPerformed
        
        
        //checking if the textfield is empty
        if(ModelNumberTxt.getText().equals("")||NameTxt.getText().equals("")||CompanyTxt.getText().equals("")||
                CostTxt.getText().equals("")||CameraTxt.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please!! Enter all the value!");
        }
        else{
            //checking if the model number repeats by calling the function of the home class
            if(CheckModelRepeat(ModelNumberTxt.getText())){
                JOptionPane.showMessageDialog(this,"Model Number cannot be same! Please try another value!");
            }
            else{
                home.AddRowToJTable(new Object[]{
                home.snsetter(),
                ModelNumberTxt.getText(),
                NameTxt.getText(),
                CompanyTxt.getText(),
                CameraTxt.getText(),
                RangeSpinner.getValue(),
                CostTxt.getText(),
                PurposeComboBox.getSelectedItem()
                });
                JOptionPane.showMessageDialog(this,"Sucessfully added!");
                close();
            }
        }
        
    }//GEN-LAST:event_AddB2ActionPerformed

