    private void CompanyTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CompanyTxtKeyPressed
        //only accept letters, space bar and delete backspace
        char c= evt.getKeyChar();
        
        if(Character.isLetter(c)||Character.isWhitespace(c)||Character.isISOControl(c)){
            CompanyTxt.setEditable(true);
        }
        else{
            CompanyTxt.setEditable(false);
            JOptionPane.showMessageDialog(this,"Special character and numbers are not accepted! Please Enter proper Company!");
        }
    }//GEN-LAST:event_CompanyTxtKeyPressed

    private void NameTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameTxtKeyPressed
        //only accept letters, space bar,number and delete backspace
        char c= evt.getKeyChar();
        
        if(Character.isLetter(c)||Character.isWhitespace(c)||Character.isISOControl(c)||Character.isDigit(c)||c=='-'){
            NameTxt.setEditable(true);
        }
        else{
            NameTxt.setEditable(false);
            JOptionPane.showMessageDialog(this,"Special character are not accepted! Please Enter proper name!");
        }
    }//GEN-LAST:event_NameTxtKeyPressed

